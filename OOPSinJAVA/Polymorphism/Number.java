package OOPSinJAVA.Polymorphism;

//  Compile time/ Static Polymorphism and
//  Achieved by method overloading
public class Number {
      int sum(int a,int b){
          return  a + b;
      }

    int sum(int a,int b,int c){
        return  a + b + c;
    }

    int sum(int a,int b,int c,int d){
        return  a + b + c + d;
    }


}

